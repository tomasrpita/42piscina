char	*ft_strcapitalize(char *str)
{
	unsigned int		i;

	i = 0;
	if (str[i] >= 'a' && str[i] <= 'z')
		str[i] -= 32;
	i++;
	while (str[i] != '\0')
	{
		if (!((str[i - 1] >= 'a' && str[i - 1] <= 'z') ||
		(str[i - 1] >= '0' && str[i - 1] <= '9') ||
		(str[i - 1] >= '\x80' && str[i - 1] <= '\xF3') ||
		(str[i - 1] >= 'A' && str[i - 1] <= 'Z')) &&
		(str[i] >= 'a' && str[i] <= 'z'))
			str[i] -= 32;
		i++;
	}
	return (str);
}
