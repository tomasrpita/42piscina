unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
  unsigned	int i;
  unsigned	int j;
  unsigned  int k;

  k = 0;
	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0' && j < size)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	while (src[k] != '\0')
		k++;
	return (k);
}
